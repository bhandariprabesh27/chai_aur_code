const month = 8;  //month can be string like "Jan" and case must string "Jan"
switch(month){
    case 1:
    console.log("January");
    break;

    case 2:
    console.log("February");
    break;

    case 3:
    console.log("March");
    break;

    case 4:
    console.log("April");
    break;

    default:
    console.log("Default Case");
}